package main.java.entities;

public class Student {
  private int id;
  private String name;
  private int form;

  {

  }

  public Student() {}

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getForm() {
    return form;
  }

  public void setForm(int form) {
    this.form = form;
  }
}
